namespace WoodClub
{
	/// <summary>
	/// Used when reporting shop usage.
	/// <see cref="ShopUsage"/>
	/// </summary>
	class Usage
	{
		/// <summary>
		/// Gets or sets the badge.
		/// </summary>
		/// <value>
		/// The badge.
		/// </value>
		public string Badge { get; set; }

		/// <summary>
		/// Gets or sets the first name.
		/// </summary>
		/// <value>
		/// The first name.
		/// </value>
		public string FirstName { get; set; }

		/// <summary>
		/// Gets or sets the last name.
		/// </summary>
		/// <value>
		/// The last name.
		/// </value>
		public string LastName { get; set; }

		/// <summary>
		/// Gets or sets the last day valid.
		/// </summary>
		/// <value>
		/// The last day valid.
		/// </value>
		public string LastDayValid { get; set; }

		/// <summary>
		/// Gets or sets the credit bank.
		/// </summary>
		/// <value>
		/// The credit bank.
		/// </value>
		public string CreditBank { get; set; }

		/// <summary>
		/// Gets or sets the shop visits.
		/// </summary>
		/// <value>
		/// The shop visits.
		/// </value>
		public string ShopVisits { get; set; }
	}
}
